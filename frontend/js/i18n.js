const I18N = (() => {
  function getLang() {
    return localStorage.getItem(CONFIG.STORAGE_KEYS.LANG) || 'fa';
  }

  function resolve(key, lang) {
    const parts = key.split('.');
    let node = I18N_DICTIONARY[lang];
    for (const p of parts) {
      if (node == null) return null;
      node = node[p];
    }
    return node;
  }

  // ترجمه یک کلید
  function t(key) {
    const lang = getLang();
    const val = resolve(key, lang) ?? resolve(key, 'en');
    return val ?? key;
  }

  // اعمال ترجمه روی صفحه
  function apply() {
    const lang = getLang();
    const dir = I18N_DICTIONARY[lang].dir;

    document.documentElement.lang = lang;
    document.documentElement.dir = dir;

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const val = t(el.getAttribute('data-i18n'));
      if (typeof val === 'string') el.textContent = val;
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const val = t(el.getAttribute('data-i18n-placeholder'));
      if (typeof val === 'string') el.placeholder = val;
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      const val = t(el.getAttribute('data-i18n-title'));
      if (typeof val === 'string') el.title = val;
    });
  }

  function setLang(lang) {
    localStorage.setItem(CONFIG.STORAGE_KEYS.LANG, lang);
    apply();
    document.dispatchEvent(new CustomEvent('i18n:changed', { detail: { lang } }));
  }

  function toggleLang() {
    setLang(getLang() === 'fa' ? 'en' : 'fa');
  }

  return { t, apply, setLang, toggleLang, getLang };
})();
